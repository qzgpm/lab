ASSUME CS:CODE,DS:DATA

DATA SEGMENT
  NUMS DW 1234H,  0ABCDH, 0F012H, 0AB12H,4567H
  COUNT DW 5
  KEY DW 0AB12H
  MSG1 DB 'FOUND$',0
  MSG2 DB 'NOT FOUND$',0
DATA ENDS

CODE SEGMENT
START:
  MOV AX, DATA
  MOV DS,AX

  MOV CX, COUNT
  MOV SI, OFFSET NUMS

SEARCH_LOOP:
  MOV AX,[SI]
  CMP AX,KEY
  JE FOUND
  ADD SI,2
  LOOP SEARCH_LOOP

NOT_FOUND:
  MOV DX,OFFSET MSG2
  MOV AH,09H
  INT 21H
  JMP EXIT

FOUND:
  MOV DX,OFFSET MSG1
  MOV AH,09H
  INT 21H

EXIT:
  MOV AH,4CH
  INT 21H
CODE ENDS
END START
