ASSUME CS:CODE,DS:DATA

DATA SEGMENT
  NUMS DW 1234H, 0ABCDH, 0F012H,0AB12H,4567H
  COUNT DW 5
DATA ENDS

CODE SEGMENT
START:
  MOV AX,DATA
  MOV DS,AX

  MOV CX,COUNT
  DEC CX

OUTER_LOOP:
  MOV SI, OFFSET NUMS
  MOV DX,CX

INNER_LOOP:
  MOV AX,[SI]
  MOV BX,[SI+2]
  CMP AX,BX
  JBE SKIP_SWAP
  XCHG AX,BX
  MOV [SI],AX
  MOV [SI+2],BX

SKIP_SWAP:
  ADD SI,2
  DEC DX
  JNZ INNER_LOOP

  LOOP OUTER_LOOP

  MOV AH,4CH
  INT 21H

CODE ENDS
END START
