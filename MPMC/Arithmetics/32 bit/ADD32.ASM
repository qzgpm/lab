ASSUME CS:CODE,DS:DATA

DATA SEGMENT
  N1 DD 72554F32H
  N2 DD 2210FECAH
  RES DW 3 DUP(?)
DATA ENDS

CODE SEGMENT
START:
  MOV AX,DATA
  MOV DS,AX
  XOR AX,AX
  MOV AX,WORD PTR N1
  ADD AX,WORD PTR N2
  MOV WORD PTR RES,AX

  MOV AX,WORD PTR N1+2
  MOV AX,WORD PTR N2+2
  MOV WORD PTR RES+2,AX
  JNB STOP
  MOV RES+4,01H
STOP:
  MOV AH,4CH
  INT 21H
CODE ENDS
END START
